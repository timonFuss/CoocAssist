import Foundation
import UIKit

/**
 Added Colors for quicker access.
 */
extension UIColor {
    struct Colors {
        static var systemOrange: UIColor  { return UIColor(red: 255/255, green: 149/255, blue: 0, alpha: 1) }
        static var systemGray: UIColor { return UIColor(red: 170/255, green: 170/255, blue: 170/255, alpha: 1) }
    }
}
